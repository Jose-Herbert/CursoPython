# Conversão de tipo, Coerção
# Type convertion, typecasting, coercion
# ato de converter um tipo com outro
# tipo imutaveis e primitivos:
# str, int, float, bool

# Operadores lógicos: + - = == != < > >= <= / // * **
print(1 + 1)
print(2 - 1)
print(2 == 2)
print(2 != 1)
print(2 / 2)
print(2 * 2)
print(2 ** 4)

